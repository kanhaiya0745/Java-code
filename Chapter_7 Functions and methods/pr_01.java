// program for average of 3 number by using function;

public class pr_01 {
    public static void avg(int a , int b, int c){
        int avg = (a+b+c)/3;
        System.out.print("The avg of 3 number is" + avg+ " ");
    }

    public static void main(String [] args){
        avg(99,99,99);

        avg(67,990,959);

        avg(55,12,99);


    }
}
